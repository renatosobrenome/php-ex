<?php
// Variáveis iniciais
$nome = "DAIANA DA SILVA";
$frase = "O medo cega os nossos sonhos.";

// Converte o nome para minúsculas
$nome_minusculo = strtolower($nome);

// Converte a frase para maiúsculas
$frase_maiuscula = strtoupper($frase);

// Exibe os resultados
echo "<h2>Conversão de Strings</h2>";
echo "Nome original: $nome <br>";
echo "Nome em minúsculas: $nome_minusculo <br><br>";

echo "Frase original: $frase <br>";
echo "Frase em maiúsculas: $frase_maiuscula";
?>
